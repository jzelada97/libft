#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	num;

	num = n;
	if (num < 0)
	{
		ft_putchar_fd('-', fd);
		num = -num;
	}
	if (num / 10)
		ft_putnbr_fd((int)(num / 10), fd);
	ft_putchar_fd((char)(num % 10 + '0'), fd);
}
